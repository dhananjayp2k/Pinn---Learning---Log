# BLOCK 2: MODEL DEFINITION
# =========================
# Architecture exactly as paper (Section 4, page 5):
#   - 3 hidden layers, 10 neurons each
#   - Sine activation function
#   - Single network taking [t̄, R_L_norm] as input
#     (paper inputs: t̄, R_L, ω — here ω is fixed at resonance so only t̄ and R_L vary)

class PiezoPINN(nn.Module):
    def __init__(self):
        super().__init__()
        # omega_0 controls SIREN internal frequency.
        # Data oscillates at ~1 rad/t̄ (resonance with dimensionless time).
        # omega_0 = 1.0 is appropriate for this frequency range.
        self.omega_0 = 1.0
        self.input_layer  = nn.Linear(2, 10)   # 2 inputs: [t̄, R_L_normalised]
        self.hidden1      = nn.Linear(10, 10)
        self.hidden2      = nn.Linear(10, 10)
        self.hidden3      = nn.Linear(10, 10)
        self.output_layer = nn.Linear(10, 2)
        self._init_weights()

    def _init_weights(self):
        # SIREN initialisation (Sitzmann et al., 2020)
        with torch.no_grad():
            self.input_layer.weight.uniform_(-1.0, 1.0)
            self.hidden1.weight.uniform_(-np.sqrt(6/10)/self.omega_0, np.sqrt(6/10)/self.omega_0)
            self.hidden2.weight.uniform_(-np.sqrt(6/10)/self.omega_0, np.sqrt(6/10)/self.omega_0)
            self.hidden3.weight.uniform_(-np.sqrt(6/10)/self.omega_0, np.sqrt(6/10)/self.omega_0)
            self.output_layer.weight.uniform_(-np.sqrt(6/10)/self.omega_0, np.sqrt(6/10)/self.omega_0)

    def forward(self, t, RL_norm):
        inp = torch.cat([t, RL_norm], dim=1)
        x   = torch.sin(self.omega_0 * self.input_layer(inp))
        x   = torch.sin(self.hidden1(x))
        x   = torch.sin(self.hidden2(x))
        x   = torch.sin(self.hidden3(x))
        out = self.output_layer(x)
        return out[:, 0:1], out[:, 1:2]   # outputs: q(t̄), Vp(t̄)

