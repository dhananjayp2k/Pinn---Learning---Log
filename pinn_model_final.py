y0     = [0.0, 0.0, 0.0]

def piezo_ode(t, y, zeta, Vs, Cp, R_L):
    q, dqdt, Vp = y
    r = Cp * R_L * omega_n
    d2qdt2 = -2*zeta*dqdt - q - k_e2*Vp + k_e2*Vs*np.sin(Omega*t)
    dVpdt  = dqdt - (1.0/r)*Vp
    return [dqdt, d2qdt2, dVpdt]

sol1 = solve_ivp(piezo_ode, t_span, y0, t_eval=t_eval, args=(true_zeta, true_Vs, true_Cp, R_L1), rtol=1e-8, atol=1e-10)
sol2 = solve_ivp(piezo_ode, t_span, y0, t_eval=t_eval, args=(true_zeta, true_Vs, true_Cp, R_L2), rtol=1e-8, atol=1e-10)

Vp_R1 = sol1.y[2] + np.random.normal(0, 0.001, len(t_eval))
Vp_R2 = sol2.y[2] + np.random.normal(0, 0.001, len(t_eval))

