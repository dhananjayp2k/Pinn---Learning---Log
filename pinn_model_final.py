epoch_history, zeta_history, Vs_history, Cp_history, loss_history = [], [], [], [], []

for epoch in range(epochs + 1):
    optimizer.zero_grad()

    zeta = torch.exp(log_zeta)
    Vs   = torch.exp(log_Vs)
    Cp   = torch.exp(log_Cp)

    # 1. Physics Loss on dense collocation points
    m1, e1 = calculate_physics_loss(model, t_physics_tensor, RL1_physics, zeta, Vs, Cp, R_L1)
    m2, e2 = calculate_physics_loss(model, t_physics_tensor, RL2_physics, zeta, Vs, Cp, R_L2)

    # 2. Data Loss on sparse experimental points
    d1 = calculate_data_loss(model, t_tensor, RL1_tensor, Vp_tensor_R1)
    d2 = calculate_data_loss(model, t_tensor, RL2_tensor, Vp_tensor_R2)

    # Total Loss Eq. (11)
    total_loss = (wf1*m1 + wf2*e1 + we*d1) + (wf1*m2 + wf2*e2 + we*d2)

    total_loss.backward()
    torch.nn.utils.clip_grad_norm_(list(model.parameters()) + [log_zeta, log_Vs, log_Cp], max_norm=1.0)
    optimizer.step()

    epoch_history.append(epoch)
    zeta_history.append(zeta.item())
