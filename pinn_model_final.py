def calculate_physics_loss(model, t, RL_norm, zeta, Vs, Cp, R_L):
    """Evaluates ODE residuals on dense collocation points (no data needed)"""
    q_pred, Vp_pred = model(t, RL_norm)

    dq_dt   = torch.autograd.grad(q_pred,  t, grad_outputs=torch.ones_like(q_pred),  create_graph=True)[0]
    d2q_dt2 = torch.autograd.grad(dq_dt,   t, grad_outputs=torch.ones_like(dq_dt),   create_graph=True)[0]
    dVp_dt  = torch.autograd.grad(Vp_pred, t, grad_outputs=torch.ones_like(Vp_pred), create_graph=True)[0]

    r = Cp * R_L * omega_n

    # Mechanical ODE
    mech_res = d2q_dt2 + 2*zeta*dq_dt + q_pred + k_e2*Vp_pred - k_e2*Vs*torch.sin(Omega*t)

    # Electrical ODE (RESTORED THE 'r' TO MATCH YOUR DATA GENERATOR!)
    elec_res = r*dq_dt - r*dVp_dt - Vp_pred

    loss_mech = torch.mean(mech_res**2)
    loss_elec = torch.mean(elec_res**2)
    return loss_mech, loss_elec

def calculate_data_loss(model, t_data, RL_norm_data, Vp_true):
    """Evaluates voltage prediction only at the sparse experimental timestamps"""
    _, Vp_pred = model(t_data, RL_norm_data)
    return torch.mean((Vp_pred - Vp_true)**2)

